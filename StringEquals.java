package com.irfan.String;

public class StringEquals {
    public static void main(String[] args){
        String s1 = "Virat";
        String s2 = "Mahi";
        String s3 = "Abd";

        System.out.println(s1.equals(s2));
        System.out.println(s2.equals(s2));
        System.out.println(s3.equals(s1));
    }
}
