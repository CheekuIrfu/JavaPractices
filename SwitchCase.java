package com.irfan.control_statement;

public class SwitchCase {
    public static void main(String[] args){
        int target = 20;
        switch(target){
            case 1 : System.out.println("Lose"); break;
            case 2 : System.out.println("Lose"); break;
            case 20 : System.out.println("Won"); break;
        }

    }
}
